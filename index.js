function recordResult(teamAId, teamBId, teamAGoals, teamBGoals){
    if(teamAGoals > teamBGoals){
        //Team A Win
        recordWin(teamAId, teamAGoals, teamBGoals)
        recordLoss(teamBId, teamBGoals, teamAGoals)

    } else if (teamBGoals > teamAGoals){
        //Team B Win
        recordWin(teamBId, teamBGoals, teamAGoals)
        recordLoss(teamAId, teamAGoals, teamBGoals)
    } else {
        //Draw
        recordDraw(teamAId, teamAGoals, teamBGoals)
        recordDraw(teamBId, teamBGoals, teamAGoals)
    }
}

function recordLoss(teamId, goalsScored, goalsAllowed){
    for(var i=0; i<teamRecords.length; i++){
        if(teamRecords[i].id == teamId){
            teamRecords[i].MP += 1;
            teamRecords[i].loss += 1;
            teamRecords[i].GF += goalsScored;
            teamRecords[i].GA += goalsAllowed;
            teamRecords[i].pts += 0;
        }
    }
}

function recordDraw(teamId, goalsScored, goalsAllowed) {
    for(var i=0; i<teamRecords.length; i++){
        if(teamRecords[i].id == teamId){
            teamRecords[i].MP += 1;
            teamRecords[i].draws += 1;
            teamRecords[i].GF += goalsScored;
            teamRecords[i].GA += goalsAllowed;
            teamRecords[i].pts += 1;
        }
    }
}

function recordWin(teamId, goalsScored, goalsAllowed){
    for(var i=0; i<teamRecords.length; i++){
        if(teamRecords[i].id == teamId){
            teamRecords[i].MP += 1;
            teamRecords[i].wins += 1;
            teamRecords[i].GF += goalsScored;
            teamRecords[i].GA += goalsAllowed;
            teamRecords[i].pts += 3;
        }
    }
}

function checkForCorner(chance){
    var rng = generateRandomNumber(1, 100)

    if(rng <= chance){
        return true;
    }
}

//Funtion determines if a shot is on or off target
function shotOnTarget(team){
    var player = pickRandomPlayerToShoot(team);
    var shotRNG = generateRandomNumber(0.5, 0.9);
    var rng = generateRandomNumber(1, 100);
    var shotOnTargetChance = player.rating * shotRNG;

    if(rng <= shotOnTargetChance){
        return true;
    } else {
        return false;
    }

}

