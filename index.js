//Function loops through entire team and decreases stamina for each player
function updatePlayerFatigue(team, pressingStyle){
    for(var i=0; i<team.length; i++){
        var args = {
            teamPress: pressingStyle,
            position: team[i].position,
            stamina: team[i].stamina
        }
        team[i].stamina = reducePlayerStamina(args)
    }
    return team;
}

//Function recieves a player's position and stamina, and reduces the player's stamina by cerain amount depending on various factors
function reducePlayerStamina(args){
    var fatigueFactor = 0;
    var pressingFactor = 0;
    var stamina = args.stamina;

    //Determine fatigue factor
    if(args.position == "GK"){
        fatigueFactor = 0.1;
    } else if(args.position == "D(C)"){
        fatigueFactor = 0.25;
    } else if(args.position == "D(R)" || args.position == "D(L)"){
        fatigueFactor = 0.3;
    } else if(args.position == "WB(R)" || args.position == "WB(L)"){
        fatigueFactor = 0.35;
    } else if(args.position == "DM(C)"){
        fatigueFactor = 0.3;
    } else if(args.position == "M(R)" || args.position == "M(L)" || args.position == "M(C)"){
        fatigueFactor = 0.4;
    } else if(args.position == "AM(R)" || args.position == "AML(R)" || args.position == "AM(C)"){
        fatigueFactor = 0.35;
    } else if(args.position == "F(C)"){
        fatigueFactor = 0.3;
    }

    //Determine pressing factor
    if(args.teamPress == "No Press"){
        pressingFactor = 0.8; //20% less stamina used
    } else if(args.teamPress == "Balanced"){
        pressingFactor = 1 //Standard stamina used
    } else if(args.teamPress == "High Press"){
        pressingFactor = 1.2; //20% more stamina used
    }

    stamina = stamina - (fatigueFactor * pressingFactor)
    
    return stamina.toFixed(2);
}

