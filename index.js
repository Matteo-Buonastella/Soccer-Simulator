//Function returns determines if player scored and returns true or false
function didTeamScore(attackingTeam, defendingTeam){
    var chance = 15; //15 percent
    var rng = Math.floor(Math.random() * (100)) + 0;  //Random Number from 1-100
    var averageAttackerRating = getAverageAttackerRating(attackingTeam);
    var keeperRating = getKeeperRating(defendingTeam)
    var differenceBetweenAttackandGK = keeperRating - averageAttackerRating
    
    chance = applyKeeperBonus(chance, differenceBetweenAttackandGK)
    //Apply +- 2% RNG
    randomNumber = generateRandomNumber(-2, 2);
    chance = chance + randomNumber

    if(rng <= chance){
        //Goal scored
        return true;
    } else {
        return false;
    }
}

//If the keeper's rating is higher than the average attacker, then reduce chance of goal and return chance
function applyKeeperBonus(chance, rating){
    //If Keeper is better than average attacker, reduce chance of goal
    if(rating >= 1){
        if(rating >= 1 && rating <= 3){
            chance = chance -1;
        } else if (rating > 3 && rating <= 5){
            chance = chance - 2;
        } else if (rating > 5 && rating <= 8){
            chance = chance - 3;
        } else if (rating > 8  && rating <= 11){
            chance = chance - 4
        } else if (rating > 11){
            chance = chance - 5
        }
    } 
    //If keeper is worse than average attacker, increase chance of goal
    else if (rating <= -1){
        if(rating <= -1 && rating >= -3){
            chance = chance + 1;
        } else if (rating < -3 && rating >= -5){
            chance = chance + 2;
        } else if (rating <- 5 && rating >= -8){
            chance = chance + 3;
        } else if (rating < -8  && rating >= -11){
            chance = chance + 4
        } else if (rating < -11){
            chance = chance + 5
        }
    }
    
    return chance;
}

