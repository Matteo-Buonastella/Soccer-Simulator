//Receives 2 squads and calculates posession seeds for both squads (including bonuses)
function calculatePosession(teamASquad, teamBSquad){
    var teamASeedOutfieldPlayers = getTeamSeedExcludingGK(teamASquad);
    var teamBSeedOutfieldPlayers = getTeamSeedExcludingGK(teamBSquad);
    var teamA_Midfield_WingBack_Average = getAverageRatingMidfielderAndWingBack(teamASquad);
    var teamB_Midfield_WingBack_Average = getAverageRatingMidfielderAndWingBack(teamBSquad);

    var difference = teamASeedOutfieldPlayers - teamBSeedOutfieldPlayers;

    //Bonus 1: Add the seed difference to better team
    if(difference > 0){
        teamASeedOutfieldPlayers +=  Math.abs(difference)
    } else if (difference < 0){
        teamBSeedOutfieldPlayers +=  Math.abs(difference)
    }

    //Bonus 2: The team that has the better average midfield/wingback gets a bonus
    if(teamA_Midfield_WingBack_Average > teamB_Midfield_WingBack_Average){
        var bonus = applySuperiorMidfieldPosessionBonus(teamA_Midfield_WingBack_Average, teamB_Midfield_WingBack_Average);
        teamASeedOutfieldPlayers += bonus;
    } else if (teamB_Midfield_WingBack_Average > teamA_Midfield_WingBack_Average){
        var bonus = applySuperiorMidfieldPosessionBonus(teamB_Midfield_WingBack_Average, teamA_Midfield_WingBack_Average);
        teamBSeedOutfieldPlayers += bonus;
    }

    //Bonus 3: Apply a small bonus to home team
    teamASeedOutfieldPlayers += 10;

    return [teamASeedOutfieldPlayers, teamBSeedOutfieldPlayers];
}

//Function recieves the average rating for both teams midfield/wingbacks and returns the bonus TeamA gets for having a superior midfield
function applySuperiorMidfieldPosessionBonus(teamA, teamB){
    var difference = teamA - teamB;

    if(difference >= 0.1 && difference < 4){
        //Bonus = 1/2 have the rating
        return Math.round(teamA/2)
    } else if(difference > 4 && difference < 10){
        //Bonus = The Rating
        return Math.round(teamA)
    } else if(difference > 10) {
        //Bonus = 2x the rating
        return Math.round(teamA * 2)
    }
}

